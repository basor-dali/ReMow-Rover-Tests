import re

def parse_nmea_log(log_data):
    lines = log_data.split('\n')
    
    parsed_data = []
    
    for line in lines:
        if not line:
            continue
        
        fields = line.split(',')
        sentence_type = fields[0][1:]
        
        if sentence_type == 'GNRMC':
            data = parse_gnrmc(fields)
        elif sentence_type == 'GNGGA':
            data = parse_gngga(fields)
        else:
            continue
        
        parsed_data.append(data)
    
    return parsed_data

def parse_gnrmc(fields):
    timestamp = fields[1]
    latitude = ddm_to_dd(fields[3], fields[4])
    longitude = ddm_to_dd(fields[5], fields[6])
    speed = float(fields[7])
    course = float(fields[8])
    
    return {
        'type': 'GNRMC',
        'timestamp': timestamp,
        'latitude': latitude,
        'longitude': longitude,
        'speed': speed,
        'course': course
    }

def parse_gngga(fields):
    timestamp = fields[1]
    latitude = ddm_to_dd(fields[2], fields[3])
    longitude = ddm_to_dd(fields[4], fields[5])
    fix_quality = int(fields[6])
    num_satellites = int(fields[7])
    altitude = float(fields[9])
    
    return {
        'type': 'GNGGA',
        'timestamp': timestamp,
        'latitude': latitude,
        'longitude': longitude,
        'fix_quality': fix_quality,
        'num_satellites': num_satellites,
        'altitude': altitude
    }

def ddm_to_dd(ddm, direction):
    ddm_parts = re.split(r'\.', ddm)
    degrees = int(ddm_parts[0][:-2])
    minutes = float(ddm_parts[0][-2:] + '.' + ddm_parts[1])
    decimal_degrees = degrees + minutes / 60
    
    if direction == 'S' or direction == 'W':
        decimal_degrees *= -1
    
    return decimal_degrees

